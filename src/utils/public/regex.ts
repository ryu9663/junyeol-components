export const regex = {
  email: /^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$/,
  password:
    /^(?=.*[a-zA-Z])(?=.*\d)(?=.*[!@#$%^&*()_+])[A-Za-z\d!@#$%^&*()_+]{8,}$/,
  slug: /^[a-z0-9]+(?:-[a-z0-9]+)*$/,
  phone: /^[+]?[(]?[0-9]{2,3}[)]?[-\s]?[0-9]{3,4}[-\s]?[0-9]{4}$/,
  number: /[^0-9]/g,
  symbols: /[^a-z0-9가-힣]/gi,
};
